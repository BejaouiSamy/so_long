#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while ((char)c != *s)
	{
		if (!*s)
			return (0);
		s++;
	}
	return ((char *)s);
}
/*Cette fonction recherche la premiere occurence
d'un caractere donnee dans une chaine. Si celui ci
est trouve, elle retourne un pointeur vers son emplacement.*/
